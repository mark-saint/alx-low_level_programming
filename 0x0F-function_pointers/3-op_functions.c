#include "3-calc.h"

/**
* op_add - fn
* @a: param
* @b: param
* Return: op
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - fn
* @a: param
* @b: param
* Return: sth
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - fn
* @a: param
* @b: param
* Return: sth
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - fn
* @a: param
* @b: param
* Return: sth
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - fn
* @a: param
* @b: param
* Return: sth
*/
int op_mod(int a, int b)
{
return (a % b);
}
